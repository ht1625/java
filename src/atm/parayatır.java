    public double setno(String no) {
        String temp = "";
        if (no.equals("")) {
            return 0;

        } else {
            for (int i = 0; i < no.length(); i++) {
                char at = no.charAt(i);
                int sayı = (at);
                if ((sayı >= 48 && sayı <= 57) || sayı == 46) {
                    temp += at;
                }
            }
            return Double.valueOf(temp);
        }
    }

